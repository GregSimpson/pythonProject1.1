# Reversed for string
seq_string = 'Python'
print(list(reversed(seq_string)))

# Reversed for tuple
seq_tuple = ('P', 'y', 't', 'h', 'o', 'n')
print(list(reversed(seq_tuple)))

# Reversed for range
seq_range = range(5, 9)
print(list(reversed(seq_range)))

# Reversed for list
seq_list = [1, 2, 4, 3, 5]
print(list(reversed(seq_list)))